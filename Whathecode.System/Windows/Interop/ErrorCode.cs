namespace Whathecode.System.Windows.Interop
{
	/// <summary>
	///   Common error codes used throughout win32.
	/// </summary>
	/// <author>Steven Jeuris</author>
	enum ErrorCode
	{
		/// <summary>
		///   Access is denied.
		/// </summary>
		AccessDenied = 0x0005,
		/// <summary>
		///   This operation returned because the time-out period expired.
		/// </summary>
		Timeout = 0x05B4
	}
}